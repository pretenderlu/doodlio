type TranslationKey = keyof typeof translations["zh-CN"];

function resolveLanguage(preference: LanguagePreference): Language {
  if (preference !== "auto") return preference;
  const languages = typeof navigator !== "undefined" ? navigator.languages?.length ? navigator.languages : [navigator.language] : [];
  return languages.some((lang) => lang.toLowerCase().startsWith("zh")) ? "zh-CN" : "en";
}

function loadPreference(): LanguagePreference {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw === "auto" || raw === "zh-CN" || raw === "en") return raw;
  } catch {
    // Ignore storage errors and use auto.
  }
  return "auto";
}

export function translateNow(key: TranslationKey, vars?: Record<string, string | number>): string {
  const language = resolveLanguage(loadPreference());
  let text: string = translations[language][key] ?? translations["zh-CN"][key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

interface I18nContextValue {
  language: Language;
  preference: LanguagePreference;
  setPreference: (preference: LanguagePreference) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [preference, setPreferenceState] = useState<LanguagePreference>(loadPreference);
  const language = resolveLanguage(preference);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const setPreference = (next: LanguagePreference) => {
    setPreferenceState(next);
    localStorage.setItem(STORAGE_KEY, next);
  };

  const value = useMemo<I18nContextValue>(() => ({
    language,
    preference,
    setPreference,
    t: (key, vars) => {
      let text: string = translations[language][key] ?? translations["zh-CN"][key] ?? key;
      if (vars) {
        for (const [name, value] of Object.entries(vars)) {
          text = text.replaceAll(`{${name}}`, String(value));
        }
      }
      return text;
    },
  }), [language, preference]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
}

